<p> <strong>Rédaction du rapport PFE</strong></p>
<p> <br>
    - Adopter le modèle uniformisé de page de garde suivant : ( <a href="fichiers/page_de_garde_pfe.doc"> Page de garde</a>)<br>
    - Insérer un résumé avec photo et mots-clé sur la couverture finale du rapport comme suit : (<a href="fichiers/page_finale_pfe.doc">Page finale </a>)<br>
    - Le rapport ne doit pas dépasser les <strong>60</strong> pages (sans annexe). L'annexe ne doit pas dépasser 20 pages.<br>
    - Le rapport doit être rédigé selon le format suivant :<br>
    · Police : Time New Roman<br>
    · Taille : 12<br>
    · Interligne : 1.5<br>
    · Marges : 2.5cm (gauche) - 2cm (droite) - 2cm (haut) - 2cm (bas)<br>
    - Le texte et les titres doivent être en noir (Sauf les figures et les images peuvent être en couleur)<br>
    - Insérer une liste des abréviations techniques (avant le sommaire).<br>
    - Insérer une liste des illustrations (Figures et tableaux) (avant le sommaire).<br>
    - Les références bibliographiques doivent être numérotées et mentionnées dans le rapport (<a href="fichiers/references.doc">voir l'exemple ici</a>).<br>
    - Le rapport doit être déposé en 3 exemplaires au secrétariat du département avant la date limite indiquée sur le calendrier.<br>
</p>
<p> <strong>Soutenance du PFE</strong> </p>
<p><br>
    - La durée maximale allouée à la présentation est <strong>20mn</strong> (Au dela de 25mn, Le jury peut interrompre la présentation)<br>
    - Le logo de l’ENSAS doit figurer sur tous les slides de la présentation (<a href="fichiers/masque.ppt">voir l'exemple ici</a>)<br>
    - Les slides doivent être numérotés <br>
</p>
<p> <strong>Evaluation du PFE</strong> </p>
<p> <br>
    - L’évaluation du PFE se base sur les 4 points suivants :<br>
    • Note de l'encadrement pédagogique : (Rapports d'avancement, Respect des délais, le sérieux, etc …)<br>
    • Note de l'encadrement professionnel : (Assiduité, Competences, Intégration dans l'entreprise, etc …)<br>
    • Note du Rapport : (Qualité du Rapport (Contenu et Forme) )<br>
    • Note de Soutenance : (Qualité de l’exposé, des diapositives, et des réponses aux questions…)<br>
</p>
<p> <strong>Etape finale</strong> </p>
<p>
    - Afin de valider le module PFE, chaque stagiaire doit déposer, au secretariat du département, une <strong>version papier finale </strong> du rapport corrigé selon les instructions du jury, muni d'un <strong>CDROM </strong> contenant tous les éléments du projet (Rapport, Présentation, et fichiers de démonstration...etc) et ce dans un delai de deux jours apres la soutenance.
