    - Téléchargez la <a  href="../../../fichiers/evaluation.pdf" target="_blank">fiche d'évaluation industrielle </a>et envoyez la à l'encadrant externe pour la remplir et la renvoyer soit par email ou/et par enveloppe confidentielle.<br>
    - Vous etes tenus d'inviter l'encadrant externe à la soutenance dès que le planning des soutenances soit affiché.
</p>
<p><strong><u>Déroulement des soutenances</strong></u></p>
<p>
    - Le Jury doit être formé d'au moins <strong>3 membres</strong> (de préference de specialités differentes).<br>
    - Téléchargez et imprimez <a target="_blank" href="../../../fichiers/fiche_jury.pdf">la Fiche de soutenance </a>dédiée au jury (à découper et à remplir par chaque membre du jury)<br>
    - La durée maximale allouée à l'éxposé est <strong>20min </strong> (Au dela de 25mn, Le jury peut interrompre la présentation)<br>
    - A la fin de chaque soutenance, veuillez rappeler à chaque groupe qu'il doit déposer la version finale du mémoire + CDROM au département dans un delai de deux jours.<br>
</p>
<p><strong><u>Evaluation des PFE</strong></u></p>
<p>
    - La note finale du PFE est la moyenne des trois notes suivantes:<br>
    • <strong>Note de l'Encadrement (1/3) </strong>: Délivrée par l'encadrant pédagogique en se basant sur l'Evaluation industrielle, l'assiduité, les rapports d'avancement et le respect des délais..etc...<br>
    • <strong>Note du Rapport (1/3) </strong>: Délivrée par tous les membres de jury en se basant sur la qualité du rapport (Contenu et Forme).<br>
    • <strong>Note de Soutenance (1/3) </strong>: Délivrée par tous les membres de jury en se basant sur la qualité de l’exposé, des slides, et des réponses aux questions…<br>
    - Lors de l'évaluation des rapports et des soutenances, veuillez vérifier que le groupe a bien respecter toutes <a  target="_blank" href="../downloads.php">les modalités </a>de rédaction et de présentation.<br>
    - Afin de cadrer et de normaliser la fourchette des notes pour tous les jurys, la note finale est plafonnée à 16/20.
</p>

